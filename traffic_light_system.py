traffic_lights = "green" #set traffic light to green
if traffic_lights == "green": #if light is green, print "go"
    print("GO!")
elif traffic_lights == "yellow": #if light is yellow, print "caution"
    print("Caution")
elif traffic_lights == "red": #if light is red, print "stop"
    print("Stop")
else:
    print("Error")